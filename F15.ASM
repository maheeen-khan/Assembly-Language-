DOSSEG
.MODEL SMALL
.STACK 100H
.DATA

STR1 DB 'I am Maheen Khan!$'
STR2 DB 'Student of 2nd year SE.$'

.CODE
MAIN PROC

MOV AX, @DATA
MOV DS, AX

MOV DX, OFFSET STR1
MOV AH,9
INT 21H

CALL enterKey

MOV DX, OFFSET STR2
MOV AH,9
INT 21H

MOV AH, 4CH
INT 21H

MAIN ENDP

enterKey PROC

MOV DX,10
MOV AH,2
INT 21H

MOV DX,13
MOV AH,2
INT 21H

RET
enterKey ENDP
END MAIN

